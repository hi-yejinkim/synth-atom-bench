"""Task registry: single source of truth for all Chinchilla scaling tasks.

Task hierarchy by complexity (1=easiest, 7=hardest):
  1. sphere_easy     — hard sphere η=0.1, pure clash avoidance
  2. sphere_medium   — hard sphere η=0.3, denser packing
  3. sphere_hard     — hard sphere η=0.5, near-maximal packing
  4. chain_N10       — polymer chain N=10, bonds + nonbonded clashes
  5. chain_N20       — polymer chain N=20, longer-range bond propagation
  6. vsepr_sp3       — VSEPR sp3 local geometry, bond angles + bond lengths
  7. sequence_linear — linear polymer N=20, global topology (long-range contacts)

Organic connection:
  sphere → chain: adds bond constraints (local connectivity)
  chain  → VSEPR: adds angular geometry (local geometry)
  VSEPR  → sequence: adds global topology (long-range contacts)
  VSEPR = local scaffold; sequence = global scaffold.

Unified metric: violation_rate ∈ [0,1] (lower=better, 0=perfect).
"""

